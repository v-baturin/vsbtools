function Mutation_M200(Ind_No)

% USPEX Version 7.4.1
global POP_STRUC
global ORG_STRUC
global OFF_STRUC
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%% CREATING Mutants by atom positions mutation%%%%%%%%%%%%%%%%%
goodAtomMutant = 0;
count = 1;
while goodAtomMutant ~= 1
    count = count + 1;
    if count > 50
       %disp('failed to do Mutation in 50 attempts, switch to Random');
       USPEXmessage(508,'',0);
       Random_M200(Ind_No);
       break;
    end
    toMutate = find(ORG_STRUC.tournament>RandInt(1,1,[0,max(ORG_STRUC.tournament)-1]));
    ind = POP_STRUC.ranking(toMutate(end));
    numIons = POP_STRUC.POPULATION(ind).numIons;
    order  =  POP_STRUC.POPULATION(ind).order;
    lat_2D = POP_STRUC.POPULATION(ind).LATTICE_2D;
    coor_2D= POP_STRUC.POPULATION(ind).COORDINATES_2D;
    numIons = POP_STRUC.POPULATION(ind).numIons;

    [MUT_COORD]= move_all_atom_Mutation(coor_2D,numIons,lat_2D, order, 0.5);
    [lat,candidate] = make2D(lat_2D, MUT_COORD, ORG_STRUC.vacuumSize(1));
    goodAtomMutant = distanceCheck (candidate,lat,numIons, ORG_STRUC.minDistMatrice);   %to reduce the cost
    if goodAtomMutant == 1
       goodAtomMutant = checkConnectivity(candidate,lat,numIons);   %to reduce the cost
    end
 
    if goodAtomMutant == 1
        OFF_STRUC.POPULATION(Ind_No).numIons = numIons;
        OFF_STRUC.POPULATION(Ind_No).LATTICE = lat;
        OFF_STRUC.POPULATION(Ind_No).COORDINATES = candidate;
        OFF_STRUC.POPULATION(Ind_No).COORDINATES_2D = MUT_COORD;
        OFF_STRUC.POPULATION(Ind_No).LATTICE_2D = lat_2D;
        info_parents = struct('parent', {},'N_Moved', {}, 'enthalpy', {});
        info_parents(1).parent = num2str(POP_STRUC.POPULATION(ind).Number);
        info_parents.N_Moved = sum(numIons);
        info_parents.enthalpy = POP_STRUC.POPULATION(ind).Enthalpies(end)/sum(numIons);
        OFF_STRUC.POPULATION(Ind_No).Parents = info_parents;
        OFF_STRUC.POPULATION(Ind_No).howCome = 'CoorMutate';
        disp(['Structure ' num2str(Ind_No) '  generated by mutation']);
    end
end
%%%%%%%%%%%%%%% END creating mutants%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
