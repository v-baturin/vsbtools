function gBmaxtrix = calcDefaultGoodBonds(atomType)


numSpecies = length(atomType);
gB        = zeros(1,numSpecies);
gBmaxtrix = zeros( numSpecies,numSpecies );

for i = 1:numSpecies
    gB(i) = atomicGoodBonds(atomType(i));
end

for i = 1:numSpecies
    for j = i:numSpecies
	gBmaxtrix(i,j) = sqrt(gB(i)*gB(j));
    end
end
 
