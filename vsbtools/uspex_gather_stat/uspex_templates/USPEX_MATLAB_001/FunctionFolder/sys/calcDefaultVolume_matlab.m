function P=VintEOS(B0,B00,V0,x)

%%  fit the Volume with the Vint-EOS
%%
%%  B00 = B0'

P=3*B0*( (1-(x/V0)^(1/3) )/ ((x/V0)^(2/3)) )*exp(1.5*(B00-1)*((1-(x/V0)^(1/3))));

end
